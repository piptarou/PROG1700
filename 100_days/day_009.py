print("===What Generation Are You?===")
year = int(input("What year were you born? "))
if year >= 1925 and year <= 1946:
  print("You're a Traditionalist")
if year >= 1947 and year <= 1964:
  print("You're a Baby Boomer")
if year >= 1965 and year <= 1981:
  print("You're Gen X")
if year >= 1982 and year <= 1995:
  print("You're a Millenial")
if year >= 1996 and year <= 2015:
  print("You're Gen Z")